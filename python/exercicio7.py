#!/usr/bin/env python3
# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário. 
def main():
    lado=float(input("Insira a medida de lado do quadrado "))
    area=lado*lado
    area =area*2
    print("O dobro da área do quadrado é de ",area)
main()
	

