#!/usr/bin/env python3
# Faça um Programa que peça a temperatura em graus Celsius, transforme e mostre em graus Farenheit. 
def main():
    C=float(input("Insira a temperatura em graus Celsius "))
    F=(C*1.8)+32
    print("A temperatura ",C," equivale a ",F," graus Farenheit")
    input("")
main()
	

